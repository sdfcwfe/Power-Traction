%% 1. 数据准备
% 第一组数据（空载电流0.25）
Ia1 = [0.3, 0.4, 0.5, 0.59];  % 电枢电流（A）
N1 = [945, 926, 857, 816, 757];  % 实际转速（rpm），对应Ia=[0.3,0.4,0.5,0.59]（注：原表中N1有5个值，需匹配Ia长度，这里取前4个N1对应Ia1）
N1 = N1(1:4);  % 匹配Ia1长度

% 第二组数据（空载电流0.26）
Ia2 = [0.3, 0.4, 0.5, 0.59];
N2 = [974, 954, 915, 878, 838];
N2 = N2(1:4);  % 匹配Ia2长度

% 额定转速（指定转数）
n_rated = 1000;  % 题目中“1000转为指定转数”


%% 2. 绘图
figure('Color','w');
plot(Ia1, N1, 'o-', 'LineWidth',1.5, 'MarkerSize',6); hold on;
plot(Ia2, N2, 's-', 'LineWidth',1.5, 'MarkerSize',6);
plot([min(Ia1) max(Ia1)], [n_rated n_rated], 'k--', 'LineWidth',1);  % 额定转速参考线

% 图形标注
xlabel('电枢电流 I_a (A)');
ylabel('实际转速 N (rpm)');
title('SPWM调速方式的机械特性曲线');
legend('空载电流0.25','空载电流0.26','额定转速1000rpm');
grid on;